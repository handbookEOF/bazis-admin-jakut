	<h2>ZAKAT</h2>
	
	<p>
	Zakat merupakan suatu kewajiban bagi umat Islam yang digunakan untuk membantu masyarakat lain, menstabilkan ekonomi masyarakat dari kalangan bawah hingga kalangan atas, sehingga dengan adanya zakat umat Islam tidak ada yang tertindas karena zakat dapat menghilangkan jarak antara si kaya dan si miskin. Oleh karena itu, zakat sebagai salah satu instrumen negara dan juga sebuah tawaran solusi untuk menbangkitkan bangsa dari keterpurukan. Zakat juga sebuah ibadah mahdhah yang diwajibkan bagi orang-orang Islam, namun diperuntukan bagi kepentingan seluruh masyarakat.
	Zakat merupakan bagian penting dalam kehidupan umat Islam. Bahkan pada masa Khalifah Abu Bakar As-Siddiq orang-orang yang enggan berzakat diperangi sampai mereka mau berzakat. Itu karena kewajiban berzakat sama dengan kewajiban mendirikan sholat.
	Zakat merupakan suatu ibadah yang dipergunakan untuk kemaslahatan umat sehingga dengan adanya zakat (baik zakat fitrah maupun zakat maal) kita dapat mempererat tali silaturahmi dengan sesama umat Islam maupun dengan umat lain.
	</p>
	
	<h3 id="pengertian_zakat">Pengertian Zakat</h3>
	<p>
	Zakat menurut bahasa artinya bersih, bertambah (<i>ziyadah</i>), dan terpuji. Jika di ucapkan, <i>zaka al-zar</i>, artinya adalah tanaman itu tumbuh dan bertambah. Jika diucapkan zakat <i>al-nafaqah</i>, artinya nafkah, tumbuh dan bertambah jika diberkati.kata ini juga sering dikemukakan untuk makna <i>thaharah</i> (suci). Allah SWT berfirman :
	<br>
	<a href="../asset/gambar/qs_asy_syams_9.png"><img src="../asset/gambar/qs_asy_syams_9.png"></a>
	<br>
	yang artinya :
	<br>
	''<i>Sesungguhnya beruntunglah orang yang menyucikan jiwa itu</i> '' (QS Asy-Syams 9)
	<br>
	<br>
	Menurut syara, zakat ialah pemberian tertentu dari harta tertentu kepada orang tertentu menurut syarat-syarat yang ditentukan. Dinamakan zakat karena di dalamnya terkandung harapan untuk memperoleh berkat, membersihkan jiwa dan menumpuknya dengan berbagai kebaikan. Kata-kata zakat itu, arti aslinya ialah tumbuh, suci, dan berkah. Firman Allah SWT dalam surat At-Taubah ayat 103.
	<br>
	<a href="../asset/gambar/qs_asy_syams_9.png"><img src="../asset/gambar/At-Taubah 103.JPG"></a>
	<br>	
	yang artinya :
	<br>
	''<i>Ambillah zakat dari harta mereka, guna membersihkan dan menyucikan mereka, dan berdoalah untuk mereka. Sesungguhnya doamu itu (menumbuhkan) ketentraman jiwa bagi mereka. Allah Maha Mendengar, Maha Mengetahui.</i> '' (QS At-Taubah 103)
	<br>
	<br>
	Maksud dari ayat diatas adalah dengan zakat itu mereka menjadi bersih dari kekikiran dan dari berlebih-lebihan dalam mencintai harta benda atau zakat itu akan menyucikan orang yang mengeluarkannya dan akan menumbuhkan pahalanya. Adapun dalan hadits diantaranya adalah :
	<br>
	<a href="../asset/gambar/hadis_zakat_1.png"><img src="../asset/gambar/hadis_zakat_1.png"></a>
	yang artinya :
	<br>
	''<i>Rasulullah sewaktu mengutus Sahabat Muadz bin Jabal ke negeri Yaman (yang telah ditaklukkan oleh umat Islam) bersabda: Engkau datang kepada kaum ahli kitab ajaklah mereka kepada syahadat, bersaksi, bahwa sesungguhnya tidak ada Tuhan selain Allah dan sesungguhnya Nabi Muhammad adalah utusan Allah. Jika mereka telah taat untuk itu, beritahulah kepada mereka bahwa Allah mewajibkan mereka melakukan sholat lima waktu dalam sehari semalam. Jika mereka telah taat untuk itu, beritahukanlah kepada mereka, bahwa Allah mewajibkan mereka menzakati kekayaan mereka. Yang zakat itu diambil dari yang kaya dan dibagi-bagikan kepada yang fakir-fakir. Jika mereka telah taat untuk itu, maka hati-hatilah (janganlah) yang mengambil yang baik-baik saja (bila kekayaan itu bernilai tinggi, sedang dan rendah, maka zakatnya harus meliputi nilai-nilai itu) hindari doanya orang yang madhlum (teraniaya) karena diantara doa itu dengan Allah tidak terdinding (pasti dikabulkan).</i> ''
	<br>
	<br>
	Zakat menurut istilah agama islam artinya sejumlah / kadar harta tertentu yang diberikan kepada yang berhak menerimanya, dengan beberapa syarat. Hukumnya zakat adalah salah satu rukun Islam yang lima, yaitu <i>fardhu ain</i> atas tiap-tiap orang yang cukup syarat-syaratnya. Zakat mulai diwajibkan pada tahun kedua <i>hijriyah</i>.
	</p>
	
	<h3 id="hukum_zakat">Hukum Zakat</h3>
	<p>
	Mengeluarkan zakat itu hukumnya wajib sebagai salah satu rukun Islam. Namun demikian, tidak semua orang yang memiliki harta terkena kewajiban zakat mal. Mengenai zakat, dapat dijumpai dalam Al-Quran di 82 ayat atau tempat, serta di dalam kitab-kitab hadits. Hal ini menunjukkan betapa pentingnya pembahasan mengenai zakat ini. Orang yang menunaikannya akan mendapatkan pahala, sedangkan yang tidak menunaikannya akan mendapat siksa. Kewajiban zakat tersebut telah ditetapkan melalui dalil-dalil qathi (pasti dan tegas) dalam Al-Quran dan Hadits serta telah disepakati oleh para ulama. Ada beberapa syarat yang harus dipenuhi, baik terkait dengan pemilik harta maupun harta itu sendiri.
	</p>
	
