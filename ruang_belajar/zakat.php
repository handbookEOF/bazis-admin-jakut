	<h3 id="tujuan_zakat">Tujuan Zakat</h3>
	<ol>
		<li>Mengangkat derajat fakir miskin dan membantunya keluar dari kesulitan hidup serta penderitaan</li>
		<li>Membantu pemecahan permasalahan yang dihadapi oleh para gharimin, ibnu sabil dan mustahiq lainnya</li>
		<li>Menolong orang yang lemah dan menderita, agar dia dapat menunaikan kewajibannya terhadap Allah dan terhadap makhluk-Nya</li>
		<li>Membentangkan dan membina tali persaudaraan sesama umat Islam dan manusia pada umumnya</li>
		<li>Menghilangkan sifat kikir pemilik harta</li>
		<li>Membersihkan sifat dengki dan iri dari hati orang-orang miskin</li>
		<li>Menjembatani jurang pemisah antara yang kaya dan yang miskin dalam masyarakat</li>
		<li>Mengembangkan rasa tanggung jawab sosial pada diri seseorang</li>
		<li>Mendidik manusia untuk berdisiplin menunaikan kewajiban dan menyerahkan hak orang lain yang ada padanya</li>
		<li>Sarana pemerataan pendapatan (rezeki) untuk mencapai keadilan sosial</li>
	</ol>
	
	<h3 id="hikmah_zakat">Hikmah Zakat</h3>
	<ol>
		<li>Membina diri untuk selalu bersyukur atas nikmat dan karuhi Allah</li>
		<li>Menumbuh suburkan harta, menggapai berkah, tambahan dan ganti dari Allah SWT. Sebagaimana firman-Nya :</li>
			<a href="../asset/gambar/hadis_zakat_1.png"><img src="../asset/gambar/QS Saba-39.png"></a><br>
			yang artinya :
			<br>
			''<i>Katakanlah: ''Sesungguhnya Tuhanku melapangkan rezki bagi siapa yang dikehendaki-Nya di antara hamba-hamba-Nya dan menyempitkan bagi (siapa yang dikehendaki-Nya)''.dan barang apa saja yang kamu nafkahkan, Maka Allah akan menggantinya dan Dia-lah pemberi rezki yang sebaik-baiknya.</i>''(QS Saba-39)
		<li>Membersihkan diri dari sifat kikir, dengki, iri, sombong serta dosa</li>
		<li>Menyucikan harta yang dimiliki</li>
		<li>Mewujudkan ras solidaritas dan kasih sayang antara sesama manusia</li>
		<li>Membina dan mengembangkan stabilitas sosial dan keadilan sosial</li>
	</ol>
	
